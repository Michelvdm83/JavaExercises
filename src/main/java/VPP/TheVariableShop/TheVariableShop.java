package VPP.TheVariableShop;

public class TheVariableShop {
    public static void main(String[] args){
        double dVar = 0.0;
        int iVar = 1;
        boolean boVar = true;
        byte byVar = 127;
        short shVar = 2;
        long lVar = 1L;
        char cVar = 'b';
        float fVar = 12F;
        String sVar = "bey";
        System.out.println(dVar);
        System.out.println(iVar);
        System.out.println(boVar);
        System.out.println(byVar);
        System.out.println(shVar);
        System.out.println(lVar);
        System.out.println(cVar);
        System.out.println(fVar);
        System.out.println(sVar);

    }
}
